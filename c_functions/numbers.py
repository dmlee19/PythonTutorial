from math import *  # math library
# Number Functions
print(abs(-5))  # 절대값 (=5)
print(pow(4, 2))  # 4^2 (=16)
print(max(2, 4, 15))  # 최대값 (=15)
print(min(2, 4, 15))  # 최소값 (=2)
print(round(3.14))  # 반올림(=3)
print(round(4.68))  # 반올림(=5)

print(floor(4.99))  # 버림(=4)
print(ceil(3.14))  # 올림(=4)
print(sqrt(16))  # 제곱근 (=4)